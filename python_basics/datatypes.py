# Creating variables of different data types
myinteger = 10  
myfloat = 10.5  
mycomplex = 2 + 3j  
mylist = [1, 2, 3]  
mytuple = (4, 5, 6)  
mydict = {"name": "Alice", "age": 25}  
myset = {7, 8, 9}  
mybool = True  

# Printing the type of each variable
print("Type of myinteger:", type(myinteger))
print("Type of myfloat:", type(myfloat))
print("Type of mycomplex:", type(mycomplex))
print("Type of mylist:", type(mylist))
print("Type of mytuple:", type(mytuple))
print("Type of mydict:", type(mydict))
print("Type of myset:", type(myset))
print("Type of mybool:", type(mybool))


# Converting int to float and vice versa

int_to_float = float(myinteger)
float_to_int = int(myfloat)

# Printing conversion results
print("Integer to float:", int_to_float)
print("Float to integer:", float_to_int)
